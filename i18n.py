def t(key: str, *args) -> str:
    text = LANGUAGES.get(_current_lang, LANGUAGES["ru"]).get(key)
    if text is None:
        text = LANGUAGES["ru"].get(key, key)
    if args:
        try:
            return text.format(*args)
        except (IndexError, KeyError):
            return text
    return text


def set_language(lang: str) -> None:
    global _current_lang
    if lang in LANGUAGES:
        _current_lang = lang


def _load_saved_language() -> None:
    try:
        try:
            from engine.paths import SETTINGS_PATH

            settings_path = SETTINGS_PATH
        except Exception:
            settings_path = os.path.join(
                os.path.dirname(os.path.abspath(__file__)), "json", "settings.json"
            )
        with open(settings_path, "r", encoding="utf-8") as f:
            data = json.load(f)
        lang = data.get("ui_language")
        if lang in LANGUAGES:
            set_language(lang)
    except Exception:
        pass


_load_saved_language()


def get_language() -> str:
    return _current_lang
